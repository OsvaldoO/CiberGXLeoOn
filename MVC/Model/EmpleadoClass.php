<?php

class EmpleadoClass{
	//Atributos
	public $codigo;
	public $nombre;
	public $rfc;
	public $telefono;
	public $tiempo;
	
	function __construct($co, $no, $rf, $te, $ti = 0)
	{
		$this->codigo = $co;
		$this->nombre = $no;
		$this->rfc = $rf;
		$this->telefono = $te;
		$this->tiempo = $ti;
	}
}
?>